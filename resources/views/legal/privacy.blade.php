    <h1>Política de Privacidad</h1>
    <p><strong>Última actualización:</strong> 28 de abril de 2026</p>

    <h2>1. Introducción</h2>
    <p>DentalFlow SaaS ("nosotros", "nuestro") se compromete a proteger la privacidad de sus datos y los de sus pacientes. Esta política describe cómo recopilamos, usamos y protegemos la información personal.</p>

    <h2>2. Información que Recopilamos</h2>
    <ul>
        <li><strong>Datos de cuenta:</strong> Nombre, email, teléfono, rol en la clínica.</li>
        <li><strong>Datos de pacientes:</strong> Historial médico, odontogramas, citas, presupuestos.</li>
        <li><strong>Datos técnicos:</strong> Dirección IP, logs de acceso, tipo de navegador.</li>
    </ul>

    <h2>3. Uso de la Información</h2>
    <p>Utilizamos la información para:</p>
    <ul>
        <li>Proveer y mejorar el Servicio.</li>
        <li>Procesar citas, presupuestos y registros clínicos.</li>
        <li>Enviar notificaciones y recordatorios.</li>
        <li>Cumplir con obligaciones legales.</li>
    </ul>

    <h2>4. Compartición de Datos</h2>
    <p>No vendemos ni compartimos sus datos con terceros, excepto:</p>
    <ul>
        <li>Con proveedores de servicios necesarios para operar el Servicio (hosting, emails).</li>
        <li>Cuando sea requerido por ley o autoridad competente.</li>
        <li>Para proteger los derechos, propiedad o seguridad de DentalFlow.</li>
    </ul>

    <h2>5. Seguridad</h2>
    <p>Implementamos medidas de seguridad técnicas y organizativas para proteger sus datos contra acceso no autorizado, alteración, divulgación o destrucción, incluyendo:</p>
    <ul>
        <li>Encriptación de contraseñas (bcrypt).</li>
        <li>Aislamiento estricto de datos entre clínicas (multi-tenancy).</li>
        <li>Registro de auditoría de accesos y cambios.</li>
        <li>Backups automáticos y seguros.</li>
    </ul>

    <h2>6. Retención de Datos</h2>
    <p>Conservamos sus datos mientras su cuenta esté activa o según sea necesario para cumplir con obligaciones legales. Puede solicitar la eliminación de sus datos contactándonos.</p>

    <h2>7. Derechos del Usuario</h2>
    <p>Dependiendo de su ubicación, puede tener derecho a:</p>
    <ul>
        <li>Acceder, rectificar o eliminar sus datos personales.</li>
        <li>Restringir u oponerse al procesamiento de sus datos.</li>
        <li>Solicitar una copia portable de sus datos.</li>
    </ul>

    <h2>8. Cumplimiento Legal</h2>
    <p>DentalFlow cumple con el Reglamento General de Protección de Datos (RGPD) y otras regulaciones aplicables. Si usted es un responsable de datos (clínica), DentalFlow actúa como procesador de datos.</p>

    <h2>9. Cookies</h2>
    <p>Utilizamos cookies esenciales para el funcionamiento del Servicio. No utilizamos cookies de rastreo ni publicitarias.</p>

    <h2>10. Contacto</h2>
    <p>Para ejercer sus derechos o realizar consultas sobre privacidad, contáctenos en: <a href="mailto:privacy@dentalflow.com">privacy@dentalflow.com</a></p>

